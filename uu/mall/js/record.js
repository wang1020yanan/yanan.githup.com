$(document).ready(function(){
    //加减积分颜色
    var l=$(".add_count").length;
    for(var i=0;i<l;i++){
       var add=$(".add_count").eq(i).text();
        if(add<0){
            $(".add_count").eq(i).css("color","#0EB621")
        }else{
            $(".add_count").eq(i).css("color","#E50F26").text("+"+add)
        }
    }
});
