$(document).ready(function(){

    $('#search').click(function(){
        $('.nav-a').hide();
        $('.nav-b').show();
    });
    $('#xx').click(function(){
        $('.nav-b').hide();
        $('.nav-a').show();
    });
    $('#yone-1').hover(function(){
        $('.menu').fadeIn();
        $('#y-1').show();
        $('#y-2').hide();
        $('#y-3').hide();
        $('#y-4').hide();
        $('#y-5').hide();
    });
    $('#yone-2').hover(function(){
        $('.menu').fadeIn();
        $('#y-2').show();
        $('#y-1').hide();
        $('#y-3').hide();
        $('#y-4').hide();
        $('#y-5').hide();
    });
    $('#yone-3').hover(function(){
        $('.menu').fadeIn();
        $('#y-3').show();
        $('#y-2').hide();
        $('#y-1').hide();
        $('#y-4').hide();
        $('#y-5').hide();
    });
    $('#yone-4').hover(function(){
        $('.menu').fadeIn();
        $('#y-4').show();
        $('#y-2').hide();
        $('#y-1').hide();
        $('#y-3').hide();
        $('#y-5').hide();
    });
    $('#yone-5').hover(function(){
        $('.menu').fadeIn();
        $('#y-5').show();
        $('#y-2').hide();
        $('#y-3').hide();
        $('#y-4').hide();
        $('#y-1').hide();
    });

    $('#cycle').hover(function(){
        $('.menu').fadeOut();
    });

    $('.mmm').hover(function(){
        $('.menu').hide();
    });
});
