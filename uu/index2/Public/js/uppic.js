$(document).ready(function(){
    $(".pic_btn").click(function(){
        $(".pic_btn2").fadeIn(0);
    });
    $("#submit").click(function(){
        var name=$("#name").val();
        var card=$("#card").val();
        var cardReg = !!card.match(/\d{17}[\d|x]|\d{15}/);
        if(cardReg==false){
            alert("不合法身份证")
        }else if(name.length<2){
            alert("请输入真实姓名")
        }else{
            $.post("http://localhost/test/yuyuetest.php",{name:name,card:card},function(e){
                if(e!==1){
                    alert("成功");
                    $("#pic").submit();
                }
            });
        }
    })
});